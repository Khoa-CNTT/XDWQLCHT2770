<script setup>
	import { ref } from 'vue'

// Ảnh chính
const mainImage = ref(null)
const mainImageName = ref('')
const mainImageSet = ref(false)

function handleMainImage(event) {
  const file = event.target.files[0]
  if (file) {
    mainImage.value = file
    mainImageName.value = file.name
    mainImageSet.value = true
  }
}

function removeMainImage() {
  mainImage.value = null
  mainImageName.value = ''
  mainImageSet.value = false
}

// Ảnh phụ
const subImages = ref([])
const subImageNames = ref([])

function handleSubImages(event) {
  const files = Array.from(event.target.files)
  for (const file of files) {
    if (!subImageNames.value.includes(file.name)) {
      subImages.value.push(file)
      subImageNames.value.push(file.name)
    }
  }
}

function removeSubImage(index) {
  subImages.value.splice(index, 1)
  subImageNames.value.splice(index, 1)
}
